#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*rtn;
	size_t	start;
	size_t	end;

	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	if (!(rtn = (char *)malloc(sizeof(char) * (end - start + 1))))
		return (NULL);
	ft_strlcpy(rtn, &s1[start], (end - start + 1));
	return (rtn);
}
